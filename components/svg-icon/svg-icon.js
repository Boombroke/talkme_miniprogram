function resolveIcon(name, filled) {
  if (!name) return [];
  var key = filled ? name : (name + '-outline');
  if (ICONS[key]) return ICONS[key];
  if (ICONS[name]) return ICONS[name];
  return [];
}

function buildSvgDataUri(iconParts, color, color2) {
  if (!iconParts.length) return '';
  var colors = [color, color2];
  var pathsStr = '';
  for (var i = 0; i < iconParts.length; i++) {
    var p = iconParts[i];
    var fill = colors[p.c] || color;
    pathsStr += '<path d="' + p.d + '" fill="' + fill + '"/>';
  }
  var svg = '<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 24 24">' + pathsStr + '</svg>';
  var arr = [];
  for (var j = 0; j < svg.length; j++) { arr.push(svg.charCodeAt(j)); }
  return 'data:image/svg+xml;base64,' + wx.arrayBufferToBase64(new Uint8Array(arr).buffer);
}

